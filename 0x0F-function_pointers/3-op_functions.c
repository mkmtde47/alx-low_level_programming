#include "3-calc.h"

/**
  * op_add - Add function
  * @a: Integer 1
  * @b: Integer 2
  * Return: Answer
  */
int op_add(int a, int b)
{
	return (a + b);
}

/**
  * op_sub - sub function
  * @a: Integer 1
  * @b: Integer 2
  * Return: Answer
  */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
  * op_mul - mul function
  * @a: Integer 1
  * @b: Integer 2
  * Return: Answer
  */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
  * op_div - div function
  * @a: Integer 1
  * @b: Integer 2
  * Return: Answer
  */
int op_div(int a, int b)
{
	return (a / b);
}

/**
  * op_mod - mod function
  * @a: Integer 1
  * @b: Integer 2
  * Return: Answer
  */
int op_mod(int a, int b)
{
	return (a % b);
}
